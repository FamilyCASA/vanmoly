for base_name, base_desc, base_module, sections in STRUCTURE:
    # 创建知识库
    r = requests.post(f"{BASE}/knowledge/bases", headers=H, json={
        "name": base_name, "description": base_desc,
        "related_module": base_module, "status": 1, "sort_order": stats["bases"]+1
    })
    base_id = r.json()["data"]["id"]
    stats["bases"] += 1
    print(f"\n📚 [{base_id}] {base_name}")

