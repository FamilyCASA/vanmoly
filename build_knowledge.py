        r = requests.post(f"{BASE}/knowledge/nodes", headers=H, json={
            "base_id": base_id, "parent_id": 0,
            "node_name": l1_name, "sort_order": stats["l1"]+1, "status": 1
        })
        l1_id = r.json()["data"]["id"]
        stats["l1"] += 1
        print(f"  📁 {l1_name}")

